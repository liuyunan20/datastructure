from typing import List


class NumArray:

    def __init__(self, nums: List[int]):
        self.nums = nums

    def update(self, index: int, val: int) -> None:
        self.nums[index] = val

    def sumRange_tle(self, left: int, right: int) -> int:
        return sum(self.nums[left: right + 1])


